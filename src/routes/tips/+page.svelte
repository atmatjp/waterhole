<h2>現金に対応している自販機</h2>
<!---->
<p>
  夢洲駅から東ゲートまでの自販機は現金に対応しています。観測した中では
  <strong>夢洲駅構内の自販機</strong>
  、
  <strong>駅構内のトイレ付近の自販機</strong>
  、
  <strong>駅を出て直ぐのトイレ裏にある自販機</strong>
  などが現金に対応していました。<br />
  その他の自販機は原則
  <strong>現金未対応</strong>
  ですので、現金で購入したい場合は、入場する前にそこいらの自販機で前もって購入しておく事をお勧めします。
</p>
